package forSub;

import java.util.Scanner;

/*
 * 2021-12-22
 * 
 * for 06. 기찍 N
 * 
 * https://www.acmicpc.net/problem/2742
 * 
 */

public class sub06 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		
		for (int i = n; i > 0; i--)
			System.out.println(i);
		
	}

}
